/**
 * Суммирует все числовые значения во вложенном объекте/массиве
 * Поддерживает любую глубину вложенности
 *
 * @param {Object|Array} obj - Объект или массив с вложенными данными
 * @param {Function|null} filterFn - Необязательная функция для фильтрации чисел
 * @returns {number} - Сумма всех числовых значений
 *
 * @example
 * sumNestedValues({a: 1, b: {c: 2, d: {e: 3}}})  // 6
 * sumNestedValues({a: [1, 2, 3], b: {c: [4, 5]}})  // 15
 * sumNestedValues({a: 1, b: 2, c: 3}, num => num > 1)  // 5 (только 2 + 3)
 */
function sumNestedValues(obj, filterFn = null) {
  // 1. Инициализируем сумму нулём
  let sum = 0;

  // 2. Перебираем ВСЕ ЗНАЧЕНИЯ объекта/массива
  //    Object.values({a: 1, b: 2}) → [1, 2]
  //    Object.values([1, 2, 3]) → [1, 2, 3]
  for (const el of Object.values(obj)) {
    // 3. Проверяем ТИП текущего значения

    // СЛУЧАЙ 1: Значение — ЧИСЛО
    if (typeof el === 'number') {
      // Проверяем фильтр (если он передан)
      if (filterFn === null || filterFn(el)) {
        //  ^^^^^^^^^^^^^^^^    ^^^^^^^^^^^
        //  фильтра нет         ИЛИ число прошло фильтр

        // Добавляем число к общей сумме
        sum += el;
      }

      // СЛУЧАЙ 2: Значение — ОБЪЕКТ или МАССИВ (вложенная структура)
    } else if (isObject(el)) {
      // РЕКУРСИЯ: вызываем ту же функцию для вложенной структуры
      // ВАЖНО: передаём filterFn дальше, чтобы фильтр работал на всех уровнях
      sum += sumNestedValues(el, filterFn);
      //     ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
      //     рекурсивный вызов вернёт сумму вложенных чисел
    }

    // СЛУЧАЙ 3: Значение — НЕ число и НЕ объект (строка, boolean, null и т.д.)
    // → ничего не делаем, просто пропускаем
  }

  // 4. Возвращаем итоговую сумму
  return sum;
}

/**
 * Проверяет, является ли значение объектом или массивом
 * (не примитивом и не null)
 *
 * @param {*} value - Любое значение
 * @returns {boolean} - true если объект/массив, false для остального
 *
 * @example
 * isObject({a: 1})    // true
 * isObject([1, 2])    // true
 * isObject(null)      // false (null — это баг JS, typeof null === 'object')
 * isObject(5)         // false
 * isObject("text")    // false
 */
function isObject(value) {
  // КРИТИЧЕСКАЯ ПРОВЕРКА:
  // typeof null === 'object' → это БАГ JavaScript!
  // Поэтому ОБЯЗАТЕЛЬНО проверяем value !== null

  return value !== null && typeof value === 'object';
  //     ^^^^^^^^^^^^^^    ^^^^^^^^^^^^^^^^^^^^^^
  //     исключаем null    проверяем тип 'object'
  //                       (массивы тоже 'object'!)
}

