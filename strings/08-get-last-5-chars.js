const lastFiveCharactersWhile = (str) => {
  let result = '';
  let start = Math.max(0, str.length - 5);
  let i = start;
  // –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –¥–ª–∏–Ω–Ω–µ–µ 5 ‚Üí –±–µ—Ä—ë–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ 5
  // –ï—Å–ª–∏ –∫–æ—Ä–æ—á–µ ‚Üí –Ω–∞—á–∏–Ω–∞–µ–º —Å 0 (–±–µ—Ä—ë–º –≤—Å—é —Å—Ç—Ä–æ–∫—É)

  while (i < str.length) {
    result += str[i];
    i++;
  }

  return result;
};

