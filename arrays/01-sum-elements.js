// –°–ø–æ—Å–æ–± 4: –ú–µ—Ç–æ–¥ forEach()
function sumArrayForEach(arr) {
  let sum = 0; // ‚úÖ –û–±—ä—è–≤–∏–ª–∏ —Å–Ω–∞—Ä—É–∂–∏
  // ‚úÖ –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç
  arr.forEach((el) => {
    sum += el; // ‚úÖ –ù–∞–∫–∞–ø–ª–∏–≤–∞–µ–º —Å—É–º–º—É
  });

  return sum; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é sum
}

