// –°–ø–æ—Å–æ–± 2: –¶–∏–∫–ª for...of
function findMaxForOf(arr) {
  let max = -Infinity; // ‚úÖ –ù–∞—á–∏–Ω–∞–µ–º —Å –º–∏–Ω—É—Å –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ—Å—Ç–∏
  // ‚úÖ –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã
  for (const num of arr) {
    // ‚úÖ –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º
    if (num > max) {
      max = num; // ‚úÖ –û–±–Ω–æ–≤–ª—è–µ–º –º–∞–∫—Å–∏–º—É–º
    }
  }
  return max; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}

