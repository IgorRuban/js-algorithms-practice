// –°–ø–æ—Å–æ–± 2: –¶–∏–∫–ª for...of
function countEvenForOf(arr) {
  let count = 0; // ‚úÖ –°—á—ë—Ç—á–∏–∫ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å 0
  // ‚úÖ –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã
  for (const num of arr) {
    if (num % 2 === 0) count++; // ‚úÖ –ü—Ä–æ–≤–µ—Ä—è–µ–º –∏ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º
  }
  return count; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}

// --------------------------------------------------------------------------------

// –°–ø–æ—Å–æ–± 3: –ú–µ—Ç–æ–¥ reduce()
function countEvenReduce(arr) {
  return arr.reduce((count, num) => (num % 2 === 0 ? (count += 1) : count), 0);
}

// -------------------------------------------------------------------------------

// –°–ø–æ—Å–æ–± 4: –ú–µ—Ç–æ–¥ filter() + length
function countEvenFilter(arr) {
  return arr.filter((num) => num % 2 === 0).length;
}

