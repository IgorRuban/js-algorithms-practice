/**
 * 📋 Задача:
 * Написать функцию repeat, которая принимает функцию и число n,
 * и вызывает функцию n раз.
 *
 * 💡 Ключевые идеи:
 * - цикл выполняет действие n раз
 * - счётчик с 0 и условие < n — ровно n итераций
 * - repeat ничего не возвращает — только вызывает callback
 * - альтернатива: рекурсия уменьшает n на 1 каждый шаг
 *
 * ⚠️ Частые ошибки:
 * - n <= arg  → выполнится n+1 раз
 * - n = arg   → присваивание вместо сравнения
 * - return fn() * arg → умножение вместо повторного вызова
 */

function repeat(fn, n) {
  for (let i = 0; i < n; i++) {
    fn();
  }
}

// --- Примеры ---

repeat(() => console.log('Hello!'), 3);
// → "Hello!"
// → "Hello!"
// → "Hello!"

repeat(() => console.log('JS!'), 2);
// → "JS!"
// → "JS!"

// 0 раз — ничего не выводит
repeat(() => console.log('Hi!'), 0);
// → ничего

const repeat = (fn, n) => n > 0 && (fn(), repeat(fn, n - 1));
