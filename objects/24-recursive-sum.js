/**
 * Подсчитывает количество числовых значений во вложенном объекте/массиве
 * (НЕ сумму, а именно КОЛИЧЕСТВО чисел)
 *
 * @param {Object|Array} obj - Объект или массив с вложенными данными
 * @returns {number} - Количество найденных чисел
 *
 * @example
 * countNestedValues({a: 1, b: {c: 2, d: {e: 3}}})  // 3 (три числа)
 * countNestedValues({a: [1, 2, 3], b: {c: 4}})     // 4 (четыре числа)
 * countNestedValues({a: 1, b: "text", c: true})    // 1 (только одно число)
 */
function countNestedValues(obj) {
  // 1. Инициализируем счётчик нулём
  let count = 0;

  // 2. Перебираем ВСЕ ЗНАЧЕНИЯ объекта/массива
  for (const el of Object.values(obj)) {
    // СЛУЧАЙ 1: Значение — ЧИСЛО
    if (typeof el === 'number') {
      // Увеличиваем счётчик на 1 (не добавляем само число!)
      count += 1;
      //       ^
      //       всегда +1, независимо от значения числа

      // СЛУЧАЙ 2: Значение — ОБЪЕКТ или МАССИВ (вложенная структура)
    } else if (isObject(el)) {
      // РЕКУРСИЯ: вызываем функцию для вложенной структуры
      // Добавляем к счётчику количество чисел из вложенной структуры
      count += countNestedValues(el);
      //       ^^^^^^^^^^^^^^^^^^^^^
      //       рекурсивный вызов вернёт количество вложенных чисел
    }

    // СЛУЧАЙ 3: Значение — НЕ число и НЕ объект
    // → пропускаем, не увеличиваем счётчик
  }

  // 3. Возвращаем итоговое количество найденных чисел
  return count;
}

