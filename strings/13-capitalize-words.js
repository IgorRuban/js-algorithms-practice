const capitalizeWordsWhile = (str) => {
  let result = ''; // –∏—Ç–æ–≥–æ–≤–∞—è —Å—Ç—Ä–æ–∫–∞
  let inWord = false; // —Ñ–ª–∞–≥ —Å–ª–æ–≤–∞
  let i = 0; // –∏–Ω–¥–µ–∫—Å

  while (i < str.length) {
    // –ï—Å–ª–∏ –ø—Ä–æ–±–µ–ª
    if (str[i] === ' ') {
      result += ' '; // –¥–æ–±–∞–≤–ª—è–µ–º –ø—Ä–æ–±–µ–ª
      inWord = false; // —Å–±—Ä–∞—Å—ã–≤–∞–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ —Å–ª–æ–≤–∞

      // –ï—Å–ª–∏ –Ω–∞—á–∞–ª–æ –Ω–æ–≤–æ–≥–æ —Å–ª–æ–≤–∞
    } else if (!inWord) {
      result += str[i].toUpperCase(); // –¥–µ–ª–∞–µ–º –±—É–∫–≤—É –∑–∞–≥–ª–∞–≤–Ω–æ–π
      inWord = true; // –≤–æ—à–ª–∏ –≤ —Å–ª–æ–≤–æ

      // –û–±—ã—á–Ω–∞—è –±—É–∫–≤–∞ –≤–Ω—É—Ç—Ä–∏ —Å–ª–æ–≤–∞
    } else {
      result += str[i]; // –¥–æ–±–∞–≤–ª—è–µ–º —Å–∏–º–≤–æ–ª
    }

    i++; // –æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º –∏–Ω–¥–µ–∫—Å
  }

  return result;
};

// console.log(capitalizeWordsWhile('hello world')); // "Hello World"
// console.log(capitalizeWordsWhile('javaScript is fun')); // "JavaScript Is Fun"

const capitalizeWordsMethods = (str) => {
  return str
    .split(' ') // —Ä–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ –º–∞—Å—Å–∏–≤ —Å–ª–æ–≤
    .map((word) => word[0].toUpperCase() + word.slice(1)) // –¥–µ–ª–∞–µ–º –ø–µ—Ä–≤—É—é –±—É–∫–≤—É –∑–∞–≥–ª–∞–≤–Ω–æ–π
    .join(' '); // —Å–æ–±–∏—Ä–∞–µ–º –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É
};

// console.log(capitalizeWordsMethods('hello world')); // "Hello World"
// console.log(capitalizeWordsMethods('javaScript is fun')); // "JavaScript Is Fun"

// —á–µ—Ä–µ–∑ replace + regex
const capitalizeWordsRegex = (str) => {
  return str.replace(/\b\w/g, (char) => char.toUpperCase());
};
