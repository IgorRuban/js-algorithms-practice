// –°–ø–æ—Å–æ–± 1: –¶–∏–∫–ª for
function countEvenFor(arr) {
  let count = 0; // ‚úÖ –°—á—ë—Ç—á–∏–∫ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å 0
  // ‚úÖ –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º —ç–ª–µ–º–µ–Ω—Ç–∞–º
  for (let i = 0; i < arr.length; i++) {
    if (arr[i] % 2 === 0) count += 1; // ‚úÖ –ü—Ä–æ–≤–µ—Ä—è–µ–º —á—ë—Ç–Ω–æ—Å—Ç—å –∏ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º
  }
  return count; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}

