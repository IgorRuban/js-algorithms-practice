function findFrequent(arr, n) {
  let count = {};

  for (let i = 0; i < arr.length; i++) {
    if (!count[arr[i]]) {
      count[arr[i]] = 1;
    } else {
      count[arr[i]] += 1;
    }
  }
  // –ò–¥—ë–º –ø–æ –º–∞—Å—Å–∏–≤—É —Å–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ
  for (let i = 0; i < arr.length; i++) {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —É —ç—Ç–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ count > n?
    if (count[arr[i]] > n) {
      return arr[i]; // –ü–µ—Ä–≤—ã–π –Ω–∞–π–¥–µ–Ω–Ω—ã–π!
    }
  }
  return null;
}

