// –°–ø–æ—Å–æ–± 4: Math.max() —Å–æ spread –æ–ø–µ—Ä–∞—Ç–æ—Ä–æ–º
function findMaxMath(arr) {
  return Math.max(...arr);
}

// ----------------------------------------------------------------------

// –°–ø–æ—Å–æ–± 5: –†–µ–∫—É—Ä—Å–∏—è
function findMaxRecursion(arr) {
  if (arr.length === 1) return arr[0]; // ‚úÖ –ë–∞–∑–æ–≤—ã–π —Å–ª—É—á–∞–π

  return Math.max(arr[0], findMaxRecursion(arr.slice(1))); // ‚úÖ –†–µ–∫—É—Ä—Å–∏—è
}

