/**
 * Находит различия между двумя объектами
 * Возвращает объект с категориями: изменённые, добавленные, удалённые свойства
 *
 * @param {Object} obj1 - Старый объект (базовое состояние)
 * @param {Object} obj2 - Новый объект (изменённое состояние)
 * @returns {Object} - Объект с различиями {changed, added, removed}
 *
 * @example
 * getDifferences(
 *   {a: 1, b: 2, c: 3},
 *   {a: 1, b: 5, d: 4}
 * )
 * // {
 * //   changed: {b: {old: 2, new: 5}},
 * //   added: {d: 4},
 * //   removed: {c: 3}
 * // }
 */
function getDifferences(obj1, obj2) {
  // Инициализируем результирующий объект с тремя категориями различий
  const result = {
    changed: {}, // Свойства, которые изменили значение
    added: {}, // Свойства, которые появились в obj2
    removed: {}, // Свойства, которые исчезли из obj1
  };

  // =========================================================================
  // ШАГ 1: Анализируем ключи из ПЕРВОГО объекта (obj1)
  // Цель: найти ИЗМЕНЁННЫЕ и УДАЛЁННЫЕ свойства
  // =========================================================================
  for (const key of Object.keys(obj1)) {
    // Проверяем, существует ли этот ключ во ВТОРОМ объекте
    if (key in obj2) {
      // ---- КЛЮЧ ЕСТЬ В ОБОИХ ОБЪЕКТАХ ----

      // Сравниваем значения по этому ключу
      if (obj1[key] !== obj2[key]) {
        // Значения РАЗНЫЕ → фиксируем изменение
        result.changed[key] = {
          old: obj1[key], // старое значение (было)
          new: obj2[key], // новое значение (стало)
        };
      }
      // Если значения ОДИНАКОВЫЕ (obj1[key] === obj2[key])
      // → ничего не делаем, свойство не изменилось
    } else {
      // ---- КЛЮЧ ЕСТЬ ТОЛЬКО В obj1, НЕТ В obj2 ----
      // Это означает, что свойство было УДАЛЕНО
      result.removed[key] = obj1[key];
    }
  }

  // =========================================================================
  // ШАГ 2: Анализируем ключи из ВТОРОГО объекта (obj2)
  // Цель: найти ДОБАВЛЕННЫЕ свойства (которых не было в obj1)
  // =========================================================================
  for (const key of Object.keys(obj2)) {
    // Проверяем, ОТСУТСТВУЕТ ли этот ключ в ПЕРВОМ объекте
    if (!(key in obj1)) {
      // ---- КЛЮЧ ЕСТЬ ТОЛЬКО В obj2, НЕТ В obj1 ----
      // Это означает, что свойство было ДОБАВЛЕНО
      result.added[key] = obj2[key];
    }

    // Если ключ ПРИСУТСТВУЕТ в obj1:
    // → мы его уже обработали в Шаге 1 (либо changed, либо unchanged)
    // → пропускаем, ничего не делаем
  }

  // Возвращаем объект с найденными различиями
  return result;
}

