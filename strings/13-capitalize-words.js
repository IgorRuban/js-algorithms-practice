const capitalizeWords = (str) => {
  let result = ''; // —Å—é–¥–∞ —Å–æ–±–∏—Ä–∞–µ–º –∏—Ç–æ–≥–æ–≤—É—é —Å—Ç—Ä–æ–∫—É
  let inWord = false; // —Ñ–ª–∞–≥: –Ω–∞—Ö–æ–¥–∏–º—Å—è –ª–∏ –º—ã –≤–Ω—É—Ç—Ä–∏ —Å–ª–æ–≤–∞

  for (let i = 0; i < str.length; i++) {
    // –ï—Å–ª–∏ –≤—Å—Ç—Ä–µ—Ç–∏–ª–∏ –ø—Ä–æ–±–µ–ª
    if (str[i] === ' ') {
      result += ' '; // –¥–æ–±–∞–≤–ª—è–µ–º –ø—Ä–æ–±–µ–ª
      inWord = false; // —Å–ª–æ–≤–æ –∑–∞–∫–æ–Ω—á–∏–ª–æ—Å—å

      // –ï—Å–ª–∏ —ç—Ç–æ –ø–µ—Ä–≤–∞—è –±—É–∫–≤–∞ –Ω–æ–≤–æ–≥–æ —Å–ª–æ–≤–∞
    } else if (!inWord) {
      result += str[i].toUpperCase(); // –¥–µ–ª–∞–µ–º –∑–∞–≥–ª–∞–≤–Ω–æ–π
      inWord = true; // —Ç–µ–ø–µ—Ä—å –º—ã –≤–Ω—É—Ç—Ä–∏ —Å–ª–æ–≤–∞

      // –ï—Å–ª–∏ —ç—Ç–æ –æ–±—ã—á–Ω–∞—è –±—É–∫–≤–∞ –≤–Ω—É—Ç—Ä–∏ —Å–ª–æ–≤–∞
    } else {
      result += str[i]; // –¥–æ–±–∞–≤–ª—è–µ–º –∫–∞–∫ –µ—Å—Ç—å
    }
  }

  return result; // –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≥–æ—Ç–æ–≤—É—é —Å—Ç—Ä–æ–∫—É
};

