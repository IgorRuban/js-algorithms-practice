/**
 * 📋 Задача:
 * Написать функцию partial, которая принимает функцию и несколько аргументов,
 * и возвращает новую функцию с уже зафиксированными первыми аргументами.
 *
 * 💡 Ключевые идеи:
 * - ...args собирает зафиксированные аргументы через closure
 * - ...newArgs собирает новые аргументы при вызове возвращаемой функции
 * - fn(...args, ...newArgs) объединяет оба набора аргументов через spread
 * - в отличие от curry — можно зафиксировать сразу несколько аргументов
 *
 * ⚠️ Частые ошибки:
 * - fn(args, newArgs) → передаём массивы а не элементы
 * - забыть ...newArgs → возвращаемая функция не принимает новые аргументы
 */

function partial(fn, ...args) {
  // принимаем функцию и зафиксированные аргументы через rest

  return function (...newArgs) {
    // возвращаем новую функцию которая принимает оставшиеся аргументы
    // newArgs — аргументы которые передадим при вызове

    return fn(...args, ...newArgs);
    // объединяем зафиксированные и новые аргументы через spread
    // и вызываем оригинальную функцию
  };
}

// --- Примеры ---

const add = (a, b, c) => a + b + c;
const add10 = partial(add, 10);

console.log(add10(5, 3)); // → 18
console.log(add10(1, 2)); // → 13

const add10and20 = partial(add, 10, 20);
console.log(add10and20(5)); // → 35
