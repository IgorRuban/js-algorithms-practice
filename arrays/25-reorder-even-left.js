console.log(separateEvenOdd([1, 2, 3, 4, 5, 6])); // [2, 4, 6, 1, 3, 5]
console.log(separateEvenOdd([5, 1, 8, 2, 7])); // [8, 2, 5, 1, 7]
console.log(separateEvenOdd([10, 15, 20, 25])); // [10, 20, 15, 25]
console.log(separateEvenOdd([1, 3, 5])); // [1, 3, 5]
console.log(separateEvenOdd([2, 4, 6])); // [2, 4, 6]
