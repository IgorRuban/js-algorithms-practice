function filterAboveAverage(arr) {
  if (arr.length === 0) return []; // —è–≤–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞

  const average = arr.reduce((a, b) => a + b, 0) / arr.length;
  return arr.filter((num) => num > average);
}

