// –°–ø–æ—Å–æ–± 1: –¶–∏–∫–ª for
function sumArray(arr) {
  let sum = 0; // ‚úÖ –ù–∞—á–∏–Ω–∞–µ–º —Å 0
  // ‚úÖ –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º —ç–ª–µ–º–µ–Ω—Ç–∞–º
  for (let i = 0; i < arr.length; i++) {
    sum += arr[i]; // ‚úÖ –ü—Ä–∏–±–∞–≤–ª—è–µ–º –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç
  }
  return sum; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}

