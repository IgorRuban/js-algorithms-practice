/**
 * 📋 Задача:
 * Написать функцию curry, которая принимает функцию с двумя аргументами
 * и возвращает каррированную версию — функцию которая принимает аргументы по одному.
 *
 * 💡 Ключевые идеи:
 * - curry возвращает функцию которая возвращает функцию
 * - первая функция принимает первый аргумент и запоминает его через closure
 * - вторая функция принимает второй аргумент и вызывает fn(arg, arg2)
 * - fn вызывается только когда известны оба аргумента
 *
 * ⚠️ Частые ошибки:
 * - fn(arg)(arg2) → fn это обычная функция а не каррированная
 * - забыть return во второй функции → вернём undefined
 */

function curry(fn) {
  // принимаем функцию с двумя аргументами

  return function (arg) {
    // возвращаем первую функцию которая принимает первый аргумент
    // arg запоминается через closure

    return function (arg2) {
      // возвращаем вторую функцию которая принимает второй аргумент
      // теперь у нас есть оба аргумента — вызываем оригинальную функцию

      return fn(arg, arg2);
      // передаём оба аргумента в fn и возвращаем результат
    };
  };
}

// --- Примеры ---

const add = (a, b) => a + b;
const curriedAdd = curry(add);

console.log(curriedAdd(2)(3)); // → 5
console.log(curriedAdd(10)(20)); // → 30

const multiply = (a, b) => a * b;
const curriedMultiply = curry(multiply);

console.log(curriedMultiply(3)(4)); // → 12
