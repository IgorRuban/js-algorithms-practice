// Базовые объекты
console.log(isEqual({ a: 1, b: 2 }, { a: 1, b: 2 })); // true ✅
console.log(isEqual({ a: 1, b: 2 }, { b: 2, a: 1 })); // true ✅ (порядок не важен)
console.log(isEqual({ a: 1, b: 2 }, { a: 1, b: 3 })); // false ✅
console.log(isEqual({ a: 1 }, { a: 1, b: 2 })); // false ✅

// Вложенные объекты
console.log(isEqual({ a: 1, b: { c: 2 } }, { a: 1, b: { c: 2 } })); // true ✅
console.log(
  isEqual(
    { a: 1, b: { c: 2, d: { e: 3 } } },
    { a: 1, b: { c: 2, d: { e: 3 } } },
  ),
); // true ✅
console.log(isEqual({ a: 1, b: { c: 2 } }, { a: 1, b: { c: 3 } })); // false ✅

// Массивы (ПОРЯДОК ВАЖЕН!)
console.log(isEqual([1, 2], [1, 2])); // true ✅
console.log(isEqual([1, 2], [2, 1])); // false ✅

// Специальные значения
console.log(isEqual(NaN, NaN)); // true ✅
console.log(isEqual(null, null)); // true ✅
console.log(isEqual(undefined, undefined)); // true ✅
console.log(isEqual(null, undefined)); // false ✅

// Date объекты
console.log(isEqual(new Date('2025-01-01'), new Date('2025-01-01'))); // true ✅

