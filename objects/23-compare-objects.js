// ============================================================================
// Задача №23: Сравнение объектов
// ============================================================================
// Условие:
// Напиши функцию, которая сравнивает два объекта и возвращает true,
// если они идентичны (одинаковые ключи и значения).
// ============================================================================

/**
 * Сравнивает два объекта на идентичность (включая вложенные объекты)
 * Поддерживает: объекты, массивы, Date, NaN, null, undefined
 *
 * @param {*} obj1 - Первое значение для сравнения
 * @param {*} obj2 - Второе значение для сравнения
 * @returns {boolean} - true если значения идентичны, false если нет
 *
 * @example
 * isEqual({a: 1, b: 2}, {a: 1, b: 2})  // true
 * isEqual({a: 1, b: 2}, {b: 2, a: 1})  // true (порядок не важен)
 * isEqual([1, 2], [2, 1])              // false (порядок важен!)
 */
function isEqual(obj1, obj2) {
  // =========================================================================
  // 0. БАЗОВЫЕ СЛУЧАИ (проверяем специальные типы в самом начале!)
  // =========================================================================

  // 0.1. Быстрая проверка на идентичность ссылок или примитивов
  //      Работает для: чисел, строк, boolean, одинаковых ссылок на объекты
  //      Примеры: 5 === 5, "text" === "text", obj === obj
  if (obj1 === obj2) {
    return true;
  }

  // 0.2. Специальная проверка для NaN
  //      Проблема: NaN === NaN → false (единственное значение в JS!)
  //      Number.isNaN() проверяет, является ли значение именно NaN
  if (Number.isNaN(obj1) && Number.isNaN(obj2)) {
    return true; // Оба NaN → считаем равными
  }

  // 0.3. Проверка на null или undefined
  //      Используем == (не ===) для проверки обоих случаев одновременно:
  //      obj == null → true если obj === null ИЛИ obj === undefined
  if (obj1 == null || obj2 == null) {
    return obj1 === obj2; // Оба должны быть одинаковыми (null или undefined)
  }

  // 0.4. Специальная проверка для Date объектов
  //      Проблема: два Date с одинаковым временем — разные объекты
  //      Решение: сравниваем время в миллисекундах через .getTime()
  if (obj1 instanceof Date && obj2 instanceof Date) {
    return obj1.getTime() === obj2.getTime();
  }

  // =========================================================================
  // 1. ОБРАБОТКА МАССИВОВ
  // =========================================================================

  // Проверяем, являются ли ОБА значения массивами
  const areBothArrays = Array.isArray(obj1) && Array.isArray(obj2);

  if (areBothArrays) {
    // 1.1. Если длины разные → массивы точно разные
    if (obj1.length !== obj2.length) {
      return false;
    }

    // 1.2. Сравниваем элементы по индексам (ПОРЯДОК ВАЖЕН!)
    //      [1, 2] ≠ [2, 1]
    for (let i = 0; i < obj1.length; i++) {
      const val1 = obj1[i];
      const val2 = obj2[i];

      // Проверяем, являются ли элементы объектами
      const areObjects = isObject(val1) && isObject(val2);

      if (areObjects) {
        // РЕКУРСИЯ: для вложенных объектов/массивов
        if (!isEqual(val1, val2)) {
          return false; // Вложенные элементы не равны → массивы разные
        }
      } else {
        // Прямое сравнение для примитивов (number, string, boolean, etc.)
        if (val1 !== val2) {
          return false; // Элементы разные → массивы разные
        }
      }
    }

    // Все элементы совпали → массивы идентичны
    return true;
  }

  // =========================================================================
  // 2. ОБРАБОТКА ОБЪЕКТОВ
  // =========================================================================

  // 2.1. Получаем массивы ключей обоих объектов
  const keys1 = Object.keys(obj1);
  const keys2 = Object.keys(obj2);

  // 2.2. Быстрая проверка: если количество ключей разное → объекты разные
  //      Оптимизация: не нужно проверять все ключи, если их количество не совпадает
  if (keys1.length !== keys2.length) {
    return false;
  }

  // 2.3. Проходим по каждому ключу первого объекта
  //      Примечание: порядок ключей НЕ важен для объектов
  //      {a: 1, b: 2} === {b: 2, a: 1}
  for (const key of keys1) {
    const val1 = obj1[key];
    const val2 = obj2[key];

    // Проверяем, являются ли ОБА значения объектами
    const areObjects = isObject(val1) && isObject(val2);

    if (areObjects) {
      // РЕКУРСИЯ: для вложенных объектов/массивов
      //           {a: {b: 1}} требует проверки вложенного {b: 1}
      if (!isEqual(val1, val2)) {
        return false; // Вложенные объекты не равны → объекты разные
      }
    } else {
      // Прямое сравнение для примитивов
      if (val1 !== val2) {
        return false; // Значения разные → объекты разные
      }
    }
  }

  // 2.4. Все проверки пройдены → объекты идентичны
  return true;
}

/**
 * Проверяет, является ли значение объектом (не примитивом)
 *
 * @param {*} value - Любое значение
 * @returns {boolean} - true если это объект или массив, false для примитивов
 *
 * @example
 * isObject({})        // true
 * isObject([])        // true
 * isObject(null)      // false (специальный случай!)
 * isObject(5)         // false
 * isObject("text")    // false
 */
function isObject(value) {
  // ВАЖНО: typeof null === 'object' — это баг JavaScript!
  // Поэтому нужна явная проверка value !== null
  //
  // Возвращаемые значения typeof:
  // typeof {}      → 'object' ✅
  // typeof []      → 'object' ✅ (массив — это тоже объект)
  // typeof null    → 'object' ❌ (баг, поэтому проверяем отдельно)
  // typeof 5       → 'number' ❌
  // typeof 'text'  → 'string' ❌
  // typeof true    → 'boolean' ❌

  return value !== null && typeof value === 'object';
}

