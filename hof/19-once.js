/**
 * 📋 Задача:
 * Написать функцию once, которая принимает функцию и возвращает новую.
 * Новая функция может быть вызвана только один раз —
 * при повторных вызовах возвращает результат первого вызова.
 *
 * 💡 Ключевые идеи:
 * - flag и result живут внутри once но снаружи возвращаемой функции — closure
 * - flag отслеживает был ли уже вызов
 * - result сохраняет результат первого вызова
 * - при повторных вызовах возвращаем result без вызова fn
 *
 * ⚠️ Частые ошибки:
 * - сохранять arg вместо fn(arg) → сохраняем аргумент а не результат
 * - забыть сохранить результат в result → при повторном вызове вернём undefined
 */

function once(fn) {
  // флаг — был ли уже вызов
  let flag = false;

  // место для хранения результата первого вызова
  let result;

  return function (arg) {
    // если функция ещё не вызывалась
    if (flag === false) {
      flag = true; // помечаем что вызов был
      return (result = fn(arg)); // вызываем fn, сохраняем и возвращаем результат
    } else {
      return result; // возвращаем сохранённый результат без повторного вызова fn
    }
  };
}

// --- Примеры ---

const init = once(() => {
  console.log('Инициализация!');
  return 42;
});

console.log(init()); // Инициализация! → 42
console.log(init()); // → 42 (без повторной инициализации!)
console.log(init()); // → 42
