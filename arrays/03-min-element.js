// –°–ø–æ—Å–æ–± 2: –¶–∏–∫–ª for...of —Å min = Infinity
function findMinForOf(arr) {
  let min = Infinity; // ‚úÖ –ù–∞—á–∏–Ω–∞–µ–º —Å –ø–ª—é—Å –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ—Å—Ç–∏
  // ‚úÖ –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã
  for (const num of arr) {
    if (num < min) min = num; // ‚úÖ –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –∏ –æ–±–Ω–æ–≤–ª—è–µ–º
  }
  return min; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}

// -------------------------------------------------------------------

// –°–ø–æ—Å–æ–± 3: –ú–µ—Ç–æ–¥ reduce()
function findMinReduce(arr) {
  return arr.reduce((min, current) => Math.min(min, current), Infinity);
}

// ---------------------------------------------------------------------

// –°–ø–æ—Å–æ–± 4: Math.min() —Å–æ spread –æ–ø–µ—Ä–∞—Ç–æ—Ä–æ–º
function findMinMath(arr) {
  return Math.min(...arr);
}

// ---------------------------------------------------------------------

// –°–ø–æ—Å–æ–± 5: –†–µ–∫—É—Ä—Å–∏—è
function findMinRecursion(arr) {
  if (arr.length === 1) return arr[0];
  return Math.min(arr[0], findMinRecursion(arr.slice(1)));
}

