// –°–ø–æ—Å–æ–± 3: –ú–µ—Ç–æ–¥ reduce()
function findMaxReduce(arr) {
  return arr.reduce((max, current) => {
    if (current > max) max = current; // ‚úÖ –û–±–Ω–æ–≤–ª—è–µ–º max
    return max; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
  }, -Infinity); // ‚úÖ –ù–∞—á–∏–Ω–∞–µ–º —Å -Infinity
}

