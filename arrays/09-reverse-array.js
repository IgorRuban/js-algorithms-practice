// –°–ø–æ—Å–æ–± 2: Spread + .reverse()
function reverseArrayRev(arr) {
  return [...arr].reverse();
}

// –ü—Ä–∏–º–µ—Ä—ã:
console.log(reverseArrayRev([1, 2, 3, 4, 5])); // [5, 4, 3, 2, 1]
console.log(reverseArrayRev(['a', 'b', 'c'])); // ['c', 'b', 'a']
console.log(reverseArrayRev([10])); // [10]
console.log(reverseArrayRev([])); // []

