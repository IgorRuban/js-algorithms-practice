/**
 * 📋 Задача:
 * Написать функцию pipe, которая принимает любое количество функций
 * и возвращает новую функцию. При вызове — передаёт аргумент
 * через все функции по порядку слева направо.
 *
 * 💡 Ключевые идеи:
 * - ...fns собирает все функции в массив
 * - возвращаем новую функцию которая принимает начальное значение
 * - reduce передаёт результат каждой функции в следующую
 * - начальное значение reduce — это входной аргумент value
 *
 * ⚠️ Частые ошибки:
 * - fns() → массив не является функцией
 * - забыть вернуть функцию → pipe вернёт undefined
 * - перепутать порядок — pipe идёт слева направо
 */

function pipe(...fns) {
  return function (value) {
    return fns.reduce((acc, fn) => fn(acc), value);
  };
}

// --- Примеры ---

const transform = pipe(
  (x) => x * 2,
  (x) => x + 1,
  (x) => x * x,
);

console.log(transform(3)); // → 49
console.log(transform(2)); // → 25
