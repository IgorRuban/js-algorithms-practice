/**
 * Группирует пользователей по полю active и трансформирует каждую группу в объект
 *
 * @param {Array} users - Массив пользователей
 * @returns {Object} - Объект вида {true: {1: {...}, 3: {...}}, false: {2: {...}}}
 *
 * @example
 * groupAndTransform(users)
 * // {
 * //   true: {
 * //     1: {name: 'Anna', score: 85},
 * //     3: {name: 'Carl', score: 78}
 * //   },
 * //   false: {
 * //     2: {name: 'Bob', score: 92}
 * //   }
 * // }
 */
function groupAndTransform(users) {
  // 1. Инициализируем результирующий объект
  //    Структура: {true: {}, false: {}}
  const result = {};

  // 2. Перебираем всех пользователей
  for (const user of users) {
    // 2.1. Определяем ключ группы (true или false)
    const groupKey = user.active;

    // 2.2. Если группы ещё нет → создаём пустой объект для неё
    //      Первый пользователь с active: true создаст result[true] = {}
    //      Первый пользователь с active: false создаст result[false] = {}
    if (!(groupKey in result)) {
      result[groupKey] = {};
    }

    // 2.3. Добавляем пользователя в соответствующую группу
    //      result[true][1] = {name: 'Anna', score: 85}
    //      result[false][2] = {name: 'Bob', score: 92}
    result[groupKey][user.id] = {
      name: user.name,
      score: user.score,
    };
  }

  // 3. Возвращаем результат
  return result;
}

