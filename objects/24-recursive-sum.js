// Базовое суммирование
console.log(sumNestedValues({ a: 1, b: { c: 2, d: { e: 3 } } })); // 6 ✅

// С массивами
console.log(sumNestedValues({ a: [1, 2, 3], b: { c: [4, 5] } })); // 15 ✅

// С фильтром (только числа > 2)
console.log(
  sumNestedValues({ a: 1, b: 2, c: { d: 3, e: 4 } }, (num) => num > 2),
); // 7 (только 3 + 4) ✅

// Подсчёт количества чисел
console.log(countNestedValues({ a: 1, b: { c: 2, d: { e: 3 } } })); // 3 ✅

