/**
 * 📋 Задача:
 * Написать функцию repeat, которая принимает функцию и число n,
 * и вызывает fn ровно n раз передавая каждый раз текущий индекс.
 *
 * 💡 Ключевые идеи:
 * - цикл от 0 до n контролирует количество вызовов
 * - fn(i) передаёт текущий индекс в каждый вызов
 * - repeat ничего не возвращает — просто вызывает fn n раз
 *
 * ⚠️ Частые ошибки:
 * - i < arr.length вместо i < n → arr не существует
 * - fn() без аргумента → индекс не передаётся
 * - return fn() * n → repeat не должен ничего возвращать
 */

function repeat(fn, n) {
  for (let i = 0; i < n; i++) {
    fn(i); // вызываем fn с текущим индексом
  }
}

// --- Примеры ---

repeat((i) => console.log(i), 3);
// → 0
// → 1
// → 2

repeat((i) => console.log(`Привет ${i}`), 3);
// → Привет 0
// → Привет 1
// → Привет 2
