// –°–ø–æ—Å–æ–± 1: –¶–∏–∫–ª for —Å max = arr[0]
function findMaxFor(arr) {
  let max = arr[0]; // ‚úÖ –ù–∞—á–∏–Ω–∞–µ–º —Å –ø–µ—Ä–≤–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞
  // ‚úÖ –¶–∏–∫–ª —Å –∏–Ω–¥–µ–∫—Å–∞ 1
  for (let i = 1; i < arr.length; i++) {
    // ‚úÖ –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º
    if (arr[i] > max) {
      max = arr[i]; // ‚úÖ –û–±–Ω–æ–≤–ª—è–µ–º –º–∞–∫—Å–∏–º—É–º
    }
  }
  return max; // ‚úÖ –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}

