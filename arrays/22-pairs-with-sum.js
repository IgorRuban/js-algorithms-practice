// Задача №22: Найти все пары чисел с заданной суммой
// Реальный кейс: Поиск комбинаций товаров на определённую сумму, анализ данных.
// Условие:
// Напиши функцию findPairs(arr, target), которая находит все уникальные пары чисел из массива, сумма которых равна target. Возвращает массив пар.

function findPairs(arr, target) {
  // функция ищет пары чисел из массива, сумма которых = target

  const result = []; // сюда будем складывать найденные пары

  const seen = new Set(); // Set хранит уникальные значения → чтобы не добавлять одинаковые пары

  for (let i = 0; i < arr.length; i++) {
    // внешний цикл — берём каждый элемент массива по очереди

    let num2 = target - arr[i]; // вычисляем какое число нужно в пару к arr[i], чтобы получить target

    // пример:
    // если target = 10
    // arr[i] = 3
    // значит num2 = 7

    for (let j = i + 1; j < arr.length; j++) {
      // внутренний цикл — ищем num2 среди элементов СПРАВА от i

      // j = i + 1 → начинаем со следующего элемента
      // чтобы:
      // - не сравнивать число само с собой
      // - не получать дубликаты типа [3,7] и [7,3]

      if (arr[j] === num2) {
        // если нашли нужное число

        const key = arr[i] + ',' + num2; // создаём уникальный ключ пары
        // например: "3,7"

        if (!seen.has(key)) {
          // если такой пары ещё не было

          result.push([arr[i], num2]); // добавляем пару в результат

          seen.add(key); // помечаем пару как уже найденную
          // теперь она не добавится второй раз
        }
      }
    }
  }

  return result; // возвращаем все найденные пары
}

// Примеры:
console.log(findPairs([1, 2, 3, 4, 5], 6));
// // [[1, 5], [2, 4]]

console.log(findPairs([1, 2, 3, 4], 10));
// // []

console.log(findPairs([2, 4, 3, 5, 7], 7));
// // [[2, 5], [4, 3]]

console.log(findPairs([1, 1, 2, 3], 4));
// // [[1, 3]]  // не дублируем пары

console.log(findPairs([], 5));
// // []
