// Условие:
// Дан массив пользователей. Нужно преобразовать его в объект,
// где ключ — id пользователя, значение — {name, score},
// и только для активных пользователей (active: true)
// ============================================================================

const users = [
  { id: 1, name: 'Anna', active: true, score: 85 },
  { id: 2, name: 'Bob', active: false, score: 92 },
  { id: 3, name: 'Carl', active: true, score: 78 },
];

// ============================================================================
// ОСНОВНАЯ ФУНКЦИЯ: Универсальное преобразование массива в объект
// ============================================================================

/**
 * Преобразует массив объектов в объект с гибкой настройкой
 *
 * @param {Array} users - Исходный массив объектов
 * @param {string|null} keyField - Поле для использования как ключ (по умолчанию 'id')
 * @param {Function|null} filterFn - Функция фильтрации (по умолчанию без фильтра)
 * @param {Function|null} mapFn - Функция преобразования значения (по умолчанию {name, score})
 * @returns {Object} - Результирующий объект
 *
 * @example
 * // Базовое использование
 * transformUsers(users)
 * // {1: {name: 'Anna', score: 85}, 3: {name: 'Carl', score: 78}}
 *
 * @example
 * // С кастомными параметрами
 * transformUsers(users, 'name', user => user.active, user => user.score)
 * // {Anna: 85, Carl: 78}
 */
function transformUsers(users, keyField = null, filterFn = null, mapFn = null) {
  // 1. Инициализируем результирующий объект
  const result = {};

  // 2. ФИЛЬТРАЦИЯ: применяем фильтр, если он передан
  //    Если filterFn === null → берём всех пользователей
  //    Если filterFn передан → фильтруем через него
  const activeUsers = filterFn === null ? users : users.filter(filterFn);

  // 3. ВАЛИДАЦИЯ: проверяем, что keyField существует у всех объектов
  //    Это предотвращает ошибки при попытке использовать несуществующее поле
  if (keyField && !activeUsers.every((el) => keyField in el)) {
    throw new Error(`Поле "${keyField}" отсутствует у некоторых объектов`);
  }

  // 4. ТРАНСФОРМАЦИЯ: перебираем отфильтрованных пользователей
  for (const el of activeUsers) {
    // 4.1. Определяем КЛЮЧ для результирующего объекта
    //      Если keyField не передан → используем 'id' по умолчанию
    const key = keyField || 'id';

    // 4.2. Определяем ЗНАЧЕНИЕ для результирующего объекта
    //      Если mapFn не передан → берём {name, score} по умолчанию
    //      Если mapFn передан → применяем функцию преобразования
    const value =
      mapFn === null ? { name: el.name, score: el.score } : mapFn(el);

    // 4.3. ОБРАБОТКА КОЛЛИЗИЙ (дубликаты ключей)
    //      Если два объекта имеют одинаковый ключ → создаём массив

    if (!(key in result)) {
      // СЛУЧАЙ 1: Ключа ещё нет → просто добавляем значение
      result[key] = value;
    } else if (!Array.isArray(result[key])) {
      // СЛУЧАЙ 2: Ключ уже есть, но это НЕ массив
      //           Значит это второй элемент с таким ключом
      //           Преобразуем в массив: [старое, новое]
      result[key] = [result[key], value];
    } else {
      // СЛУЧАЙ 3: Ключ уже есть и это массив
      //           Значит это третий+ элемент
      //           Добавляем в существующий массив
      result[key].push(value);
    }
  }

  // 5. Возвращаем результирующий объект
  return result;
}

// ============================================================================
// МИНИ 4: Обратная трансформация (объект → массив)
// ============================================================================

/**
 * Преобразует объект обратно в массив объектов
 * Добавляет поле id из ключа объекта
 *
 * @param {Object} obj - Объект вида {1: {name: 'Anna', score: 85}}
 * @returns {Array} - Массив объектов [{id: 1, name: 'Anna', score: 85}]
 *
 * @example
 * objectToArray({1: {name: 'Anna', score: 85}, 3: {name: 'Carl', score: 78}})
 * // [
 * //   {id: 1, name: 'Anna', score: 85},
 * //   {id: 3, name: 'Carl', score: 78}
 * // ]
 */
function objectToArray(obj) {
  // 1. Инициализируем результирующий массив
  const result = [];

  // 2. Перебираем объект через Object.entries()
  //    Object.entries() возвращает массив пар [ключ, значение]:
  //    {1: {name: 'Anna'}} → [['1', {name: 'Anna'}]]
  for (const [key, value] of Object.entries(obj)) {
    // 3. Создаём новый объект:
    //    - id: преобразуем ключ из строки в число
    //    - ...value: разворачиваем все свойства из value
    result.push({
      id: Number(key), // '1' → 1
      ...value, // {name: 'Anna', score: 85}
    });
    // Результат: {id: 1, name: 'Anna', score: 85}
  }

  // 4. Возвращаем массив
  return result;
}

