// –°–ø–æ—Å–æ–± 1: –¶–∏–∫–ª for (—Å –∫–æ–Ω—Ü–∞ –∫ –Ω–∞—á–∞–ª—É)
function reverseArray(arr) {
  let result = [];
  for (let i = arr.length - 1; i >= 0; i--) {
    result.push(arr[i]); // ‚úÖ –ò–¥—ë–º —Å –∫–æ–Ω—Ü–∞, –¥–æ–±–∞–≤–ª—è–µ–º –≤ –Ω–æ–≤—ã–π –º–∞—Å—Å–∏–≤
  }
  return result;
}

