// –°–ø–æ—Å–æ–± 5: (–†–µ–∫—É—Ä—Å–∏—è)
const sumArrayRecursion = (arr) => {
  if (arr.length === 0) return 0;

  return arr[0] + sumArrayRecursion(arr.slice(1));
};

